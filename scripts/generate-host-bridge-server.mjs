#!/usr/bin/env node

import * as fs from "fs/promises"
import * as path from "path"
import * as grpc from "@grpc/grpc-js"
import * as protoLoader from "@grpc/proto-loader"
import chalk from "chalk"

const SERVER_SETUP_FILE = path.resolve("src/generated/standalone/host-bridge-server-setup.ts")
const DESCRIPTOR_SET = path.resolve("dist-standalone/proto/descriptor_set.pb")

const typeNameToFQN = new Map()

function addTypeNameToFqn(name, fqn) {
	if (typeNameToFQN.has(name)) {
		throw new Error(`Proto type ${name} redefined (${fqn}).`)
	}
	typeNameToFQN.set(name, fqn)
}

function getFqn(name) {
	if (!typeNameToFQN.has(name)) {
		throw Error(`No FQN for ${name}`)
	}
	return typeNameToFQN.get(name)
}

/**
 * Main function to generate the host bridge server setup
 */
async function main() {
	// Load service definitions from descriptor set
	const descriptorBuffer = await fs.readFile(DESCRIPTOR_SET)
	const packageDefinition = protoLoader.loadFileDescriptorSetFromBuffer(descriptorBuffer)
	const proto = grpc.loadPackageDefinition(packageDefinition)

	// Extract host services and proto messages from the proto definition
	const hostServices = {}
	for (const [name, def] of Object.entries(proto.host)) {
		if (def && "service" in def) {
			hostServices[name] = def
		} else {
			addTypeNameToFqn(name, `proto.host.${name}`)
		}
	}
	for (const [name, def] of Object.entries(proto.cline)) {
		if (def && !("service" in def)) {
			addTypeNameToFqn(name, `proto.cline.${name}`)
		}
	}

	// Generate server setup file
	await generateServerSetupFile(hostServices)

	console.log(`Generated host bridge server setup at:`)
	console.log(`- ${SERVER_SETUP_FILE}`)
}

/**
 * Generate the server setup file for host bridge services
 */
async function generateServerSetupFile(hostServices) {
	const imports = [
		`import * as grpc from "@grpc/grpc-js"`,
		`import * as proto from "@shared/proto/index"`,
		`import { host } from "@generated/grpc-js"`,
		`import { HostBridgeProvider } from "@/standalone/host-bridge-provider"`,
		`import { GrpcHandlerWrapper, GrpcStreamingResponseHandlerWrapper } from "@/standalone/grpc-types"`,
	]

	const serviceSetups = []
	for (const [serviceName, serviceDefinition] of Object.entries(hostServices)) {
		const methods = Object.entries(serviceDefinition.service)
			.map(([methodName, methodDef]) => {
				const requestType = getFqn(methodDef.requestType.type.name)
				const responseType = getFqn(methodDef.responseType.type.name)
				const isStreaming = methodDef.responseStream
				// Map service names to their property names in HostBridgeProvider
				const servicePropertyMap = {
					CommandService: "commandService",
					EnvService: "envService",
					TerminalService: "terminalService",
					WatchService: "watchService",
					WindowService: "windowService",
					WorkspaceService: "workspaceService",
				}
				const servicePropertyName = servicePropertyMap[serviceName] || `${serviceName.toLowerCase()}Service`

				if (isStreaming) {
					return `        ${methodName}: wrapStreamingHandler(
            (request: ${requestType}, responseStream: any, requestId?: string) => hostBridgeProvider.${servicePropertyName}.${methodName}(request, responseStream, requestId)
        )`
				} else {
					return `        ${methodName}: wrapper(
            (request: ${requestType}) => hostBridgeProvider.${servicePropertyName}.${methodName}(request)
        )`
				}
			})
			.join(",\n")

		serviceSetups.push(`    // ${serviceName}
    server.addService(host.${serviceName}Service, {
${methods}
    })`)
	}

	const content = `// GENERATED CODE -- DO NOT EDIT!
// Generated by scripts/generate-host-bridge-server.mjs
${imports.join("\n")}

export function addHostBridgeServices(
    server: grpc.Server,
    hostBridgeProvider: HostBridgeProvider,
    wrapper: GrpcHandlerWrapper,
    wrapStreamingHandler: GrpcStreamingResponseHandlerWrapper
): void {
${serviceSetups.join("\n\n")}
}
`

	// Write output file
	await fs.mkdir(path.dirname(SERVER_SETUP_FILE), { recursive: true })
	await fs.writeFile(SERVER_SETUP_FILE, content)
}

// Run the main function
main().catch((error) => {
	console.error(chalk.red("Error:"), error)
	process.exit(1)
})
